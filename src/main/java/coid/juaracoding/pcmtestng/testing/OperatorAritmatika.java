public class OperatorAritmatika {

    public double tambah(double val1,double val2){
        return val1+val2;
    }

    public double kurang(double val1,double val2){
        return val1-val2;
    }

    public double bagi(double val1,double val2){
        return val1/val2;
    }
    public double kali(double val1,double val2){
        return val1*val2;
    }
    public double modulus(double val1,double val2){
        return val1%val2;
    }
}
