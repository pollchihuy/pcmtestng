public class OperatorLogika {

    public boolean logikaAnd(boolean val1, boolean val2){

        return val1&&val2;
    }

    public boolean logikaOr(boolean val1, boolean val2){

        return val1||val2;
    }
    
    public boolean logikaNot(boolean val1){

        return !val1;
    }

}
